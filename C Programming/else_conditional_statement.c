#include <stdio.h>

int main() {
    // declare variables
    int num = 6;

    if(num == 5) { // check if num is equal to 5
        // run everything inside {} if num is equal to 5
        printf("num is equal to 5");
    } else {
        // run everything inside {} if 'if' expression is false
        printf("num is not equal to 5");
    }

    // exit program
    return 0;
}
