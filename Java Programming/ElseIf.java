public class ElseIf {
    public static void main(String[] args) {
        int num = 4;

        if (num == 6) {
            System.out.println("Num is equal to 6");
        } else if (num == 4) {
            System.out.println("Num is not equal to 6, but equal to 4");
        } else {
            System.out.println("Num is not equal to 6");
        }
    }
}
