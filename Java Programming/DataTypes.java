public class DataTypes {
    public static void main(String[] args) {
        int num = 5; // Integer (whole number)
        float floating = 5.99f; // Floating point number
        double dbl = 3.14; // Double
        char letter = 'D'; // Character
        boolean trueFalse = true; // Boolean
        String word = "Hello"; // String
        // output different types
        System.out.println("int num = " + num);
        System.out.println("float floating = " + floating);
        System.out.println("double dbl = " + dbl);
        System.out.println("char letter = " + letter);
        System.out.println("boolean trueFalse = " + trueFalse);
        System.out.println("String word = " + word);
    }
}
